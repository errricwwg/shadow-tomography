# Warning extraction from notes
# в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ

# Keywords in notes that should be surfaced as warnings.
# Each entry is (substring_to_match, warning_prefix).
_WARNING_TRIGGERS: List[Tuple[str, str]] = [
    ("snap",     "Parameter snapping occurred"),
    ("failed",   "Estimation failure"),
    ("skipped",  "Observable skipped"),
    ("warning",  "Backend warning"),
    ("extrapolat", "Potential extrapolation"),
    ("may not have been trained", "Out-of-distribution risk"),
]

_SHADOWGPT_GENERAL_CAVEAT = (
    "Results were estimated from synthetic shadows generated by the learned ShadowGPT "
    "model, not from the exact quantum state. Estimates carry statistical uncertainty "
    "and depend on how well the training distribution covers these parameters."
)


def _collect_warnings_from_notes(notes: List[str], backend: str) -> List[str]:
    """
    Scan *notes* and return a list of human-readable warning strings.

    Rules:
    - Any note matching a ``_WARNING_TRIGGERS`` keyword is surfaced with a
      standardised prefix.
    - If the backend is ``"shadowgpt"``, append a general learned-model caveat.
    - Each warning is de-duplicated by prefix so repeat triggers do not flood.
    """
    warnings: List[str] = []
    seen_prefixes: set = set()

    for note in notes:
        low = note.lower()
        for keyword, prefix in _WARNING_TRIGGERS:
            if keyword in low and prefix not in seen_prefixes:
                seen_prefixes.add(prefix)
                warnings.append(f"{prefix}: {note.strip()}")

    if backend == "shadowgpt" and _SHADOWGPT_GENERAL_CAVEAT not in warnings:
        warnings.append(_SHADOWGPT_GENERAL_CAVEAT)

    return warnings


