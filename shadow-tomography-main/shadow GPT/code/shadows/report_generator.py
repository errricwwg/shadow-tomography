def make_explanation_result(result: PropertyResult) -> ExplanationResult:
    """
    Convert *result* into a fully populated ``ExplanationResult``.

    Calls ``make_short_summary``, ``make_detailed_summary``, and
    ``_collect_warnings_from_notes`` and assembles the schema object.

    Parameters
    ----------
    result : PropertyResult
        Output of ``evaluate_exact`` or ``evaluate_with_shadowgpt``.

    Returns
    -------
    ExplanationResult
        With ``short_summary``, ``detailed_summary``, and ``warnings`` populated.
    """
    backend  = _detect_backend(result.notes)
    short    = make_short_summary(result)
    detailed = make_detailed_summary(result)
    warnings = _collect_warnings_from_notes(result.notes, backend)
    return ExplanationResult(
        short_summary=short,
        detailed_summary=detailed,
        warnings=warnings,
    )


