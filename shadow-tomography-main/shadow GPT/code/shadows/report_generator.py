# Public: make_short_summary
# в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ

def make_short_summary(result: PropertyResult) -> str:
    """
    Return a one-to-two sentence high-level summary of *result*.

    Covers:
    - Hamiltonian family and key parameters.
    - The most physically informative available observable (energy, then
      magnetization, then correlations).
    - Source attribution (exact vs. ShadowGPT).

    Parameters
    ----------
    result : PropertyResult
        Populated result object from the exact or learned backend.

    Returns
    -------
    str
        One-to-two sentence plain-English summary.
    """
    family     = result.family
    params     = result.params
    n_qubits   = result.n_qubits
    backend    = _detect_backend(result.notes)
    back_phrase = _backend_phrase(backend)

    # Identify the Hamiltonian briefly.
    spec = get_family_spec(family)
    family_label = spec.canonical_name.upper() if spec else family.upper()

    param_str = ", ".join(f"{k}={v:.2f}" for k, v in sorted(params.items()))
    id_clause = f"{n_qubits}-qubit {family_label} ({param_str})"

    # Pick the most informative single observable for the short summary.
    obs_clause = ""
    if result.energy is not None:
        per_site = result.energy / n_qubits if n_qubits > 0 else result.energy
        obs_clause = (
            f"The ground-state energy is {result.energy:.4f} "
            f"({per_site:.4f} per site)."
        )
    elif result.magnetization is not None:
        m = result.magnetization
        abs_m = abs(m)
        if abs_m < _MAG_NEAR_ZERO:
            mag_desc = "little net Z magnetization"
        elif abs_m < _MAG_WEAK:
            mag_desc = f"weak Z magnetization ({m:.3f})"
        else:
            mag_desc = f"significant Z magnetization ({m:.3f})"
        obs_clause = f"The system shows {mag_desc}."
    elif result.correlations is not None:
        c = result.correlations
        abs_c = abs(c)
        if abs_c < _CORR_VERY_WEAK:
            corr_desc = "very weak nearest-neighbor ZZ correlations"
        elif abs_c < _CORR_MODEST:
            corr_desc = "modest nearest-neighbor ZZ correlations"
        else:
            corr_desc = "relatively strong nearest-neighbor ZZ correlations"
        obs_clause = f"The system shows {corr_desc} ({c:.3f})."

    summary = f"Ground-state properties of the {id_clause}, {back_phrase}."
    if obs_clause:
        summary = f"{summary} {obs_clause}"
    return summary


# в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ
# Public: make_detailed_summary
# в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ

def make_detailed_summary(result: PropertyResult) -> str:
    """
    Return a multi-sentence detailed explanation of all observables in *result*.

    Structure:
    1. Hamiltonian identity sentence.
    2. Family-specific physical context (phase, regime).
    3. Energy sentence (if available).
    4. Magnetization sentence (if available).
    5. Correlations sentence (if available).
    6. RГ©nyi-2 entropy sentence (if available).
    7. Backend attribution sentence.

    Parameters
    ----------
    result : PropertyResult
        Populated result object from the exact or learned backend.

    Returns
    -------
    str
        Multi-sentence plain-English explanation.
    """
    family   = result.family
    params   = result.params
    n_qubits = result.n_qubits
    backend  = _detect_backend(result.notes)

    sentences: List[str] = []

    # 1. Hamiltonian identity
    sentences.append(_describe_family(family, params, n_qubits))

    # 2. Family-specific physical context
    phase_note = _family_phase_note(family, params)
    if phase_note:
        sentences.append(phase_note)

    # 3вЂ“6. Observable sentences
    energy_sent = _describe_energy(result.energy, family, n_qubits)
    if energy_sent:
        sentences.append(energy_sent)

    mag_sent = _describe_magnetization(result.magnetization, family, params)
    if mag_sent:
        sentences.append(mag_sent)

    corr_sent = _describe_correlations(result.correlations, family, n_qubits)
    if corr_sent:
        sentences.append(corr_sent)

    renyi_sent = _describe_renyi2(result.renyi2_entropy, n_qubits)
    if renyi_sent:
        sentences.append(renyi_sent)

    # 7. Backend attribution
    back_phrase = _backend_phrase(backend)
    if backend == "exact":
        sentences.append(
            f"All values were {back_phrase} (numpy.linalg.eigh), "
            f"which is numerically exact for systems of this size."
        )
    elif backend == "shadowgpt":
        n_shadows_note = ""
        for note in result.notes:
            if "n_shadows=" in note:
                import re
                m = re.search(r"n_shadows=(\d+)", note)
                if m:
                    n_shadows_note = f" using {m.group(1)} synthetic shadow measurements"
                    break
        sentences.append(
            f"All values were {back_phrase}{n_shadows_note}. "
            f"Shadow-based estimates carry statistical uncertainty."
        )
    else:
        sentences.append(f"Values were {back_phrase}.")

    return " ".join(sentences)


