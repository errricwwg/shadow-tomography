"""
report_generator.py вЂ” Deterministic natural-language explanation layer.

Converts a ``PropertyResult`` (from the exact or learned ShadowGPT backend)
into human-readable summaries and structured ``ExplanationResult`` objects.
All output is template-based; no LLM is involved.

Public API
----------
make_short_summary(result)       в†’ one-to-two sentence high-level summary.
make_detailed_summary(result)    в†’ multi-sentence explanation of all observables.
make_explanation_result(result)  в†’ ExplanationResult combining both + warnings.

Convenience wrappers (parse в†’ infer в†’ report):
explain_exact(text)              в†’ ExplanationResult via exact backend.
explain_with_shadowgpt(text, **) в†’ ExplanationResult via learned backend.
"""

from __future__ import annotations

from typing import Dict, List, Optional, Tuple

from .nl_schema import ExplanationResult, PropertyResult
from .family_registry import get_family_spec


# в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ
# Physical interpretation thresholds
# в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ

# |magnetization| buckets
_MAG_NEAR_ZERO   = 0.05   # below this в†’ "little net magnetization"
_MAG_WEAK        = 0.30   # below this в†’ "weak net magnetization"
                           # above в†’ "significant net magnetization"

# |ZZ correlation| buckets
_CORR_VERY_WEAK  = 0.10
_CORR_MODEST     = 0.30

# RГ©nyi-2 entropy buckets (nats)
_S2_NEAR_PRODUCT = 0.10   # below в†’ "near-product state"
_S2_MODERATE     = 0.50   # below в†’ "moderate entanglement"
                           # above в†’ "significant entanglement"

# TFIM: criticality is at |h/J| = 1 in the thermodynamic limit.
# Flag when within this fractional band of the critical point.
_TFIM_CRITICAL_TOL = 0.15


# в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ
# Backend detection
# в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ

def _detect_backend(notes: List[str]) -> str:
    """
    Infer which backend produced *result* by scanning its notes.

    Returns ``"exact"``, ``"shadowgpt"``, or ``"unknown"``.
    """
    for note in notes:
        low = note.lower()
        if "exact diagonalization" in low:
            return "exact"
        if "shadowgpt" in low or "autoregressive" in low or "generated shadows" in low:
            return "shadowgpt"
    return "unknown"


def _backend_phrase(backend: str) -> str:
    """Return a short source-attribution phrase for inline use."""
    if backend == "exact":
        return "computed by exact diagonalization"
    if backend == "shadowgpt":
        return "estimated from synthetic shadows generated by the ShadowGPT model"
    return "computed"


# в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ
# Family description
# в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ

def _describe_family(family: str, params: Dict[str, float], n_qubits: int) -> str:
    """
    Return a plain-English sentence identifying the Hamiltonian.

    Uses the registry's ``short_description`` plus a formatted parameter list.
    Falls back to a generic sentence if the family is not in the registry.
    """
    spec = get_family_spec(family)

    # Build a readable parameter string: "J=1.00, h=0.80"
    param_str = ", ".join(f"{k}={v:.2f}" for k, v in sorted(params.items()))

    if spec is not None:
        # Use the first sentence of short_description (up to first full stop).
        desc_sentence = spec.short_description.split(".")[0].strip()
        return (
            f"This is a {n_qubits}-qubit {desc_sentence} "
            f"with parameters {param_str} and open boundary conditions."
        )

    return (
        f"This is a {n_qubits}-qubit '{family}' Hamiltonian "
        f"with parameters {param_str} and open boundary conditions."
    )


def _tfim_phase_note(params: Dict[str, float]) -> Optional[str]:
    """
    Return a one-sentence note about the TFIM phase, or None.

    Remarks on the ferromagnetic / paramagnetic / near-critical regime based
    on the ratio |h/J|.  Only meaningful for TFIM.
    """
    J = params.get("J", 1.0)
    h = params.get("h", 0.5)
    if J == 0.0:
        return None
    ratio = abs(h / J)
    if abs(ratio - 1.0) <= _TFIM_CRITICAL_TOL:
        return (
            f"With |h/J| = {ratio:.2f}, the system is near the quantum critical point "
            f"(|h/J| = 1), where the model transitions between ferromagnetic and "
            f"paramagnetic phases."
        )
    if ratio < 1.0:
        return (
            f"With |h/J| = {ratio:.2f} < 1, the system is in the ferromagnetic phase, "
            f"where ZZ ordering tends to dominate over transverse-field fluctuations."
        )
    return (
        f"With |h/J| = {ratio:.2f} > 1, the system is in the paramagnetic phase, "
        f"where the transverse field dominates and spins tend to align along X."
    )


def _xxz_phase_note(params: Dict[str, float]) -> Optional[str]:
    """Return a brief XXZ phase note based on delta."""
    delta = params.get("delta", 1.0)
    if abs(delta) < 0.05:
        return "With delta в‰€ 0, this reduces to the XX model (no ZZ anisotropy)."
    if abs(delta - 1.0) < 0.05:
        return "With delta = 1, this is the isotropic Heisenberg (XXX) chain."
    if delta < 0.0:
        return (
            f"With delta = {delta:.2f} < 0, ZZ interactions are antiferromagnetic "
            f"relative to the XX/YY terms."
        )
    return (
        f"With delta = {delta:.2f}, the model has enhanced ZZ anisotropy "
        f"relative to the XX/YY exchange."
    )


def _heisenberg_phase_note(params: Dict[str, float]) -> Optional[str]:
    """Return a brief Heisenberg phase note based on J sign."""
    J = params.get("J", 1.0)
    if J > 0:
        return (
            f"With J = {J:.2f} > 0, the chain is antiferromagnetic; "
            f"adjacent spins prefer to anti-align."
        )
    if J < 0:
        return (
            f"With J = {J:.2f} < 0, the chain is ferromagnetic; "
            f"adjacent spins prefer to align."
        )
    return None


def _family_phase_note(family: str, params: Dict[str, float]) -> Optional[str]:
    """Dispatch to the correct per-family phase note helper."""
    if family == "tfim":
        return _tfim_phase_note(params)
    if family == "xxz":
        return _xxz_phase_note(params)
    if family == "heisenberg":
        return _heisenberg_phase_note(params)
    return None


