def explain_exact(text: str) -> ExplanationResult:
    """
    Parse *text*, run exact diagonalization, and return an ``ExplanationResult``.

    Equivalent to::

        result = parse_and_evaluate_exact(text)
        return make_explanation_result(result)

    Parameters
    ----------
    text : str
        Free-text Hamiltonian description, e.g. ``"4-qubit TFIM J=1 h=0.8"``.

    Returns
    -------
    ExplanationResult

    Raises
    ------
    ValueError
        If the description cannot be parsed into a fully supported Hamiltonian.
    """
    from .inference_engine import parse_and_evaluate_exact

    result = parse_and_evaluate_exact(text)
    return make_explanation_result(result)


def explain_with_shadowgpt(text: str, **kwargs) -> ExplanationResult:
    """
    Parse *text*, run the ShadowGPT learned backend, and return an ``ExplanationResult``.

    Equivalent to::

        result = parse_and_evaluate_with_shadowgpt(text, **kwargs)
        return make_explanation_result(result)

    Keyword arguments are forwarded to ``evaluate_with_shadowgpt``
    (``checkpoint_dir``, ``n_shadows``, ``temperature``, ``device``, ``seed``).

    Parameters
    ----------
    text : str
        Free-text Hamiltonian description.

    Returns
    -------
    ExplanationResult

    Raises
    ------
    ImportError
        If PyTorch is not installed.
    ValueError
        If the description cannot be parsed.
    FileNotFoundError
        If the checkpoint directory is not found.
    """
    from .inference_engine import parse_and_evaluate_with_shadowgpt

    result = parse_and_evaluate_with_shadowgpt(text, **kwargs)
    return make_explanation_result(result)


