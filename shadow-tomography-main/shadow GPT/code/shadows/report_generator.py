# Observable description helpers
# в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ в”Ђв”Ђ

def _describe_energy(
    energy: Optional[float],
    family: str,
    n_qubits: int,
) -> Optional[str]:
    """
    Return a plain-English sentence describing the ground-state energy, or None.
    """
    if energy is None:
        return None
    per_site = energy / n_qubits if n_qubits > 0 else energy
    return (
        f"The ground-state energy is {energy:.4f} "
        f"(approximately {per_site:.4f} per site for {n_qubits} qubits)."
    )


def _describe_magnetization(
    magnetization: Optional[float],
    family: str,
    params: Dict[str, float],
) -> Optional[str]:
    """
    Return a plain-English sentence describing the average Z magnetization, or None.
    """
    if magnetization is None:
        return None
    m = magnetization
    abs_m = abs(m)
    sign_phrase = "positive" if m > 0 else ("negative" if m < 0 else "zero")

    if abs_m < _MAG_NEAR_ZERO:
        qualitative = "little net magnetization along Z (consistent with a disordered or balanced state)"
    elif abs_m < _MAG_WEAK:
        qualitative = f"weak {sign_phrase} net magnetization along Z"
    else:
        qualitative = f"significant {sign_phrase} net magnetization along Z"

    # Add family-specific context for hz (longitudinal field polarizes spins)
    hz_note = ""
    if family == "ising_general":
        hz = params.get("hz", 0.0)
        if abs(hz) > 0.05:
            direction = "positive" if hz > 0 else "negative"
            hz_note = (
                f" The longitudinal field hz = {hz:.2f} tends to polarize spins "
                f"in the {direction} Z direction."
            )

    return (
        f"The average Z magnetization is {m:.4f}, indicating {qualitative}.{hz_note}"
    )


def _describe_correlations(
    correlations: Optional[float],
    family: str,
    n_qubits: int,
) -> Optional[str]:
    """
    Return a plain-English sentence describing nearest-neighbor ZZ correlations, or None.
    """
    if correlations is None:
        return None
    if n_qubits < 2:
        return "Nearest-neighbor correlations are not defined for a single-qubit system."

    c = correlations
    abs_c = abs(c)
    sign_phrase = "ferromagnetic (positive ZZ)" if c > 0 else "antiferromagnetic (negative ZZ)"

    if abs_c < _CORR_VERY_WEAK:
        strength = "very weak"
    elif abs_c < _CORR_MODEST:
        strength = "modest"
    else:
        strength = "relatively strong"

    return (
        f"The average nearest-neighbor ZZ correlation is {c:.4f}, "
        f"which is {strength} and {sign_phrase} in character."
    )


def _describe_renyi2(
    renyi2_entropy: Optional[float],
    n_qubits: int,
) -> Optional[str]:
    """
    Return a plain-English sentence describing the RГ©nyi-2 half-chain entropy, or None.
    """
    if renyi2_entropy is None:
        return None
    s2 = renyi2_entropy
    k = max(1, n_qubits // 2)

    if s2 < _S2_NEAR_PRODUCT:
        qualitative = (
            "very small, suggesting the state is close to a product state "
            "with little quantum entanglement across the bipartition"
        )
    elif s2 < _S2_MODERATE:
        qualitative = (
            "moderate, indicating a nontrivial degree of quantum entanglement "
            "across the bipartition"
        )
    else:
        qualitative = (
            "relatively large, indicating significant quantum entanglement "
            "across the bipartition"
        )

    return (
        f"The RГ©nyi-2 entropy of the half-chain bipartition "
        f"(first {k} of {n_qubits} qubits) is {s2:.4f}, which is {qualitative}."
    )


